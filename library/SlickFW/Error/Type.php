/**
 * Type
 * @category  SlickFW\Error
 * @package   SlickFW
 */

namespace SlickFW\Error;

class Type
{
    /**
     * return name of given error-type
     * @param int $type
     * @return string
     */
    public static function getName($type = E_ERROR)
    {
        switch ($type) {
            default:
                // default is 'Error'
            case E_ERROR:
                // Errors
            case E_CORE_ERROR:
                // Errors
            case E_COMPILE_ERROR:
                // Errors
            case E_USER_ERROR:
                // should only be generated by the parser
            case E_PARSE:
                $return = 'Error';
                break;
            case E_WARNING:
                // Warnings
            case E_CORE_WARNING:
                // Warnings
            case E_COMPILE_WARNING:
                // Warnings
            case E_USER_WARNING:
                $return = 'Warning';
                break;
            case E_NOTICE:
                // notices
            case E_USER_NOTICE:
                $return = 'Notice';
                break;
            case E_STRICT:
                $return = 'Strict-Warning';
                break;
            case E_RECOVERABLE_ERROR:
                $return = 'Uncaught Error';
                break;
            case E_DEPRECATED:
                // Deprecated Warnings
            case E_USER_DEPRECATED:
                $return = 'Deprecated';
                break;
        }
        return $return;
    }

}